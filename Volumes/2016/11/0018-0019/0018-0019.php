			<p>उड्डयनाय भ्रमरे या शक्ति: आवश्यकी तस्या: वर्धनाय स्वस्य अन्त: औष्ण्यं नियन्त्र्य कमलिनी अतिथिसत्कारं करोति&nbsp;। न केवलं तावत्, स्वादपूर्णस्य आहारस्य दानं, तस्य जीर्णतासम्पादनाय अपेक्षितस्य औष्णस्य व्यवस्थापनं च विधाय, पुंस्त्रीसमागमाय मृदुलं केसरमयं मञ्चम्, अनुकूलं परिसरं च उपकल्पयति&nbsp;। एवं कमलिनी भ्रमरदम्पत्यो: मधुचन्द्रास्वादाय उष्णतानियन्त्रणादि- व्यवस्थां परिकल्प्य परागस्पर्शक्रियां  साधयति&nbsp;। प्रियासङ्गमसिद्धि: भवति इत्यत: भ्रमर: बन्धनमपि प्रियं   मन्यते&nbsp;। भ्रमरस्य प्रियासङ्गमाय अनुकूलौष्ण्यव्यवस्थां परिकल्पयत् कमलं भ्रमरम् आकर्षत् स्वसन्ततिवृद्धिं साधयति&nbsp;। परागस्पर्शानन्तरं कमले औष्ण्यपरिमाणं   क्षीयते&nbsp;। एतस्मात् पुष्पं विशीर्णं भवति&nbsp;। ‘पुष्पं पर्युषितं त्यजन्ति मधुपा:’ इति वदन्ती चाणक्यनीति: विज्ञानसिद्धं तत्त्वं कविशैल्या प्रतिपादयति&nbsp;।</p>
			<p>‘अरविन्दमशोकं च चूतं च नवमल्लिका &nbsp;। नीलोत्पलं च पञ्चैते पञ्चबाणस्य सायका:’ इत्यत्र कमलमपि मदनस्य बाणत्वेन अभि-हितम्&nbsp;। आधुनिके काले कमलं मदनस्य बाणत्वेन परिगणनम् अर्हेत्, न अर्हेत् वा, किन्तु तत् भ्रमरदम्पत्यो: मधुचन्द्ररसास्वादे उपकारकं भवति, औष्ण्यनियमनादिक्रियया भ्रमरस्य आकर्षणे समर्थं भवति च इत्यत्र नास्ति सन्देह:&nbsp;।</p>
