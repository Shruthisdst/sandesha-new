			<p><span class="bigfont">य</span>त्र कमलिन्या: प्रस्ताव: स्यात् तत्र भ्रमरस्यापि प्रस्ताव: 	भवेत्एव प्राय:&nbsp;। कमलिनीभ्रमरयो: व्यवहार: कवीनां वर्णनेषु दृश्यते बहुधा&nbsp;।</p>
			<p>
				रात्रिर्गमिष्यति भविष्यति सुप्रभातम्<br />
				भास्वानुदेष्यति हसिष्यति पङ्कजश्री:&nbsp;।<br />
				इत्थं विचिन्तयति कोषगते द्विरेफे<br />
				हा हन्त हन्त नलिनीं गज उज्जहार ॥
			</p>
			<p>इत्यत्र गजेन कृतं नलिन्या: हरणं विवृण्वन् सुभाषितकार: वदति यत् कमलिन्या: मुकुलितताम् अवगच्छन् अपि भ्रमर: तत: उड्डीय न गत: इति&nbsp;। शुक्रनीति: वदति -</p>
			<p>
				उत्कर्तितुं समर्थोऽपि गन्तुं चैव सपक्षक:&nbsp;।<br />
				द्विरेफो गन्धलोभेन कमले याति बन्धनम् ॥
			</p>
			<p>‘कमलदलानां कर्तने भ्रमरस्य न कोऽपि क्लेश:&nbsp;। उड्डीय गमनाय तस्य पक्षौ अपि समर्थौ भवत:&nbsp;। तथापि स: कमले बन्धनम् अभ्युपगत्य तदन्त: बन्धी भवति&nbsp;। एतादृशात् बन्धनात् अधिक: पुष्परस: प्राप्यते इति स: जानाति’ इति वदति कवि:&nbsp;। एवं च प्रियाया: प्रेमबन्धनं सर्वेषाम् अपि प्रियमेव ननु ?</p>
			<p>चाणक्यनीति: वदति - ‘अलिरयं नलिनीदलमध्यग: कमलिनीमकरन्दमदालस:’ इति&nbsp;। अत्रापि नलिनीबन्धनस्थेन भ्रमरेण क्रियमाण: अमन्दरसास्वाद: एव प्रतिपाद्यते कविना&nbsp;।</p>
			<p>एवं कमलिनीभ्रमरयो: सम्बन्ध: कविभि: बहुभि: बहुधा वर्ण्यते&nbsp;। भ्रमराय पुष्परसं दत्त्वा तद्द्वारा परागस्पर्शक्रियां कारयत् पुष्पं स्वसन्तानवृद्धिं साधयति इति यत् लोके सिद्धं तत्र अनुरागव्यवहारं पश्यन्ति कवय:&nbsp;। अचेतनेषु अपि चेतनत्वारोप: कवीनां सहज: गुण:&nbsp;।</p>
			<p>‘भृङ्गा: पुनर्विकचपद्मवने वसन्ति’ इति कथयन्ती चाणक्यनीति: पुन: वदति -</p>
			<p>
				बन्धनानि खलु सन्ति बहूनि प्रेमरज्जुदृढबन्धनमन्यत्&nbsp;।<br />
				दारुभेदनिपुणोऽपि षडङ्घ्रिर्निष्क्रियो भवति पङ्कजकोषे ॥
			</p>
			<p>‘य: काष्ठभेदने अपि समर्थ: स: भ्रमर: प्रेमरज्जुबन्धनम् अवाप्य कमलिन्या: अङ्के सुखेन स्वपिति’ इति विवृण्वन् नीतिकार: प्रेमबन्धनस्य बलवत्तां प्रतिपादयति&nbsp;। रणे असङ्ख्यान् अरीन् निर्दयं दलयन् अपि भट: प्रियाया: अङ्के तु अनुरागसुखम् अनुभवन् सुखेन स्वपिति यत् तत्र किमाश्चर्यम् ?</p>
			<p>कमलिनी भ्रमरस्य अनुरागपात्रम्&nbsp;। अत: मकरन्दग्रहणसमये भ्रमर: तथा व्यवहरति, येन कमलिन्या: अल्पा अपि वेदना न स्यात्&nbsp;। तथा हि चाणक्यनीतिशास्त्रम् - ‘यथा क्रमेण गृह्णन्ति पुष्पेभ्यो मधु षट्पदा:’ इति&nbsp;।</p>
			<p>कविभि: दृष्टा: एते सर्वविधा: अनुरागव्यवहारा: तु कमलिनीमधुकरयो: मानवव्यवहारारोपात्&nbsp;। विज्ञानं तु एतम् अनुरागारोपं न अभ्युपगच्छेत् चेदपि तयो: परस्परपूरकतां तु अवश्यमेव प्रतिपादयति&nbsp;। आष्टे्रलियादेशस्य अडिलेड्विश्व-विद्यालयस्य प्राणिविज्ञानविभागीयौ प्राध्यापकौ प्रा.रोजर् एस्. सेमूर्, प्रा.पाल षुल्ट्ज् मोटिल् इत्येतौ एतद्विषये विशेषाध्ययनं कृतवन्तौ स्त:&nbsp;। तयो: एतद्विषयक: संशोधनलेख: लण्डनीयायां ‘फिलासफिकल् ट्रान्साक्शन् आफ रायल् सोसैटी’ इत्यस्यां पत्रिकायां (1998, 353:935-943) प्रकाशित: अस्ति&nbsp;। कमलपुष्पेण क्रियमाणम् औष्ण्यनियन्त्रणम् एव अस्य संशोधनस्य विषय:&nbsp;। विशिष्टे कालविशेषे, भ्रमर: तदीयस्य अन्त: यदा भवति तदा च कमलपुष्पे औष्ण्यस्य चयापचयप्रक्रिया प्रचलति विशेषत: इति वदत: तौ&nbsp;।</p>
			<p>ऋत्वनुगुणं सस्यानि स्वव्यवहारे परिवर्तनं कल्पयन्ति इति तु निसर्गनियम:&nbsp;। पारिजातं, मल्लिका, रात्रिराज्ञी, ब्रह्मकमलम् इत्यादय: निशाप्रियतां दर्शयन्ति&nbsp;। सूर्यकान्तिपुष्पं सूर्यस्य गतिम् अनुसरति&nbsp;। कमलपुष्पं विकासकाले अधिकम् औष्ण्यं  जनयति&nbsp;। तस्य औैष्ण्यस्य नियन्त्रणम् अपि जानाति तत्&nbsp;। वातावरणस्य उष्णता दशसेण्टिग्रेड्मिता त्रिंशत्सेण्टिग्रेड्मिता वा स्यात् चेदपि पुष्पस्य उष्णता तु समस्थितौ एव भवति&nbsp;। वातावरणस्य उष्णता यदा क्षीणा भवति तदा पुष्पस्य अक्षकाण्ड: (पुष्पाधार:) स्वस्य चयापचयकार्यस्य वेगं वर्धयित्वा स्वीयोष्णताया: समतां सम्पादयति&nbsp;।</p>
