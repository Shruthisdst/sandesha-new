			<p>एतस्य चयापचयकार्यस्य पूरकतया श्वासोच्छ्वासवेगस्य वधर्र्नाय आम्लजनकसेवनप्रमाणे अपि वृद्धि: जायते&nbsp;। वातावरणस्य उष्णता (Ta - ambient emperature), अक्षकाण्डस्य उष्णता (Tr - recepacular emperature), आम्लजनकसेवन प्रमाणम् इत्येतान् निरूपयत् साङ्केतिकचित्रं पश्यत&nbsp;।</p>
			<p>एतस्मिन् चित्रे पुष्पस्य उष्णतानियन्त्रणक्रिया तस्य विकासस्य अरम्भात् आरभ्य शलाकाग्रकार्यनिर्वर्तनपर्यन्तमपि अनुवर्तते इत्येतत् द्रष्टुं शक्नुम:&nbsp;। शलाकाग्रस्य (Stigma) कार्यं तु परागं स्वीकृत्य अण्डाशयं प्रति शलाकानलिकया प्रापणम्&nbsp;।</p>
			<p>कमलपुष्पं द्विलिङ्गि&nbsp;। पुष्पे आदौ अण्डाशय: पक्वदशां प्राप्य परागस्पर्शार्थं भ्रमरम् आकर्षति&nbsp;। पुष्पस्य स्त्रीकोषाणां पुंकोषाणां च परिशीलनात् ज्ञायते यत् पुष्पे स्त्रीकोष: एव (Carpels) अधिकसङ्ख्यया भवन्ति इति&nbsp;।</p>
			<p>पुंकोषरूपा: केसरा: पुष्पकाण्डं परित: दलेषु संलग्ना: दृश्यन्ते&nbsp;। यदा पुष्पं पूर्णत: विकसेत् तदा ते दलात् पृथग्भवन्ति&nbsp;। पूर्णत: विकसितात् पुष्पात् परागा: यदा पतन्ति तदा पुष्पे औष्ण्यपरिमाणम् आम्लजनकसेवनक्रिया च न्यूनतां याति&nbsp;।</p>
			<p>पुष्पविकासे पञ्च स्तरा: भवन्ति&nbsp;। ते च -</p>
			<ol class="olist">
				<li>मुकुलाग्रं सुसम्पृक्तदलं सत् सूच्यग्रमिव दृश्यते&nbsp;।</li>
				<li>दलानि मुकुलस्य अग्रभागे ईषत् विकसन्ति&nbsp;।</li>
				<li>दलानां किञ्चिदधिकविकास:, किन्तु मुकुलाकारस्य  अनपगम:&nbsp;।</li>
				<li>दलानाम् अधिकविकास:, पुष्पस्य स्थालिकाकारता च&nbsp;।</li>
				<li>दलानां समग्रविकास:, केसरयुक्तस्य मध्यभागस्य स्पष्टदर्शनम्&nbsp;।</li>
			</ol>
			<p>प्रा.समोर: वदति यत् कमलं भ्रमरदम्पत्यो: मधुरमेलनस्य शय्या भवति इति&nbsp;। कमलपुष्पस्य विकासारम्भस्य द्वितीये दिने भ्रमरदम्पती कमलपुष्पं प्राप्नुत:&nbsp;। तौ तत्र कमले जायमानं बन्धनम् अपि अविगणय्य मधुचन्द्रास्वादे मग्नौ भवत:&nbsp;। अनन्तरदिने सूर्योदयकाले परागान् शरीरे धृत्वा उड्डयनाय सज्जौ भवत:&nbsp;।</p>
			<div class="leftimg">
				<img src="main/img3.jpg" alt="image" title="कमलिनी कर्षति भ्रमरम्">
				<p>कृष्णपट्टिका: रात्रिं, श्वेतपट्टिका: दिवाकालं च सङ्केतयन्ति&nbsp;।<br /> पुष्पस्य जीवितावधिं निरूपयति चित्रमेतत्&nbsp;।</p>
				<p class="left">
					आम्लजनकसेवनप्रमाणम्<br />
					(मि.मी.) प्रतिनिमेषम् , उष्णता (सेण्टिग्रेड्मानेन)<br />
					शाखोत्पत्ति:<br />
					Ta - वातावरणस्य उष्णता<br />
