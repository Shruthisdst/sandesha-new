				<p>
					पुष्पधारस्य उष्णता सेण्टिगे्रड्मानेन<br />
					वातावरणस्य उष्णता (सेण्टिग्रेडमानेन)<br />
					सामान्यवातावरणे पुष्पाणि<br />
